
public class ConditionalIF {

	public static void main(String[] args) {
		// Conditional if and else Operators

		int a = 1;
		int b = 2;

		if (a == b) {

			// The below line will only be executed if a==b is true, if false.. it skips the
			// line

			System.out.println("a is equal to b");
		}

		else {
			// If none of the conditions are met, below line will be executed
			System.out.println("a is not equal to b");
		}

	}

}
