
public class ConditionalifExercise2 {

	public static void main(String[] args) {

		// Conditional if, else if and else conditions

		// Write a program to test three values to find out if 20 is greater than 18 and 16
		// If the condition A is true, print "x is the greatest"
		// If the condition B is true, print "y is the greatest"
		// Else print "z is greatest"

		int x = 18;
		int y = 20;
		int z = 16;

		if (x > y) {
			System.out.println("x is the greatest");
		}

		else if (y > z) {
			System.out.println("y is the greatest");
		}

		else {
			System.out.println("z is the greatest");

		}

	}

}
